using UnityEngine;
using System.Collections;
using CORE;

//********************************************************************************************************
//
//********************************************************************************************************

public class MapNotice : MonoBehaviour, IMapNotice
{
	//****************************************************************************************************
	//
	//****************************************************************************************************

	UnityEngine.UI.Text m_text = null;

	//****************************************************************************************************
	//
	//****************************************************************************************************

	public void Awake()
	{
		m_text = CORE.HIERARCHY.FindComp< UnityEngine.UI.Text >( gameObject, "MAP_COPYRIGHTS" );
	}

	//****************************************************************************************************
	//
	//****************************************************************************************************

	public string text
	{
		get { return ( m_text != null ) ? m_text.text : string.Empty; }

		set { if( m_text != null ) m_text.text = value; gameObject.SetActive( string.IsNullOrEmpty( value ) == false ); }
	}
}

